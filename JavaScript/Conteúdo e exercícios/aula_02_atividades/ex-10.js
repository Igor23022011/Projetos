let num = "10";
let num2 = "5";

// A soma é feita normalmente.
console.log(10 + 5)

/* Por conta do 10 estar entre aspas, o que o transforma num texto,
 o programa apenas junta ambos os números, formando 105. */
console.log(num + 5)

// Acontece a mesma coisa, porém, o 5 está entre aspas.  
console.log(10 + num2)

/* Aqui, por ambos estarem entre aspas, 
o programa não efetua a conta, e, novamente, apenas os junta. */
console.log(num + num2)